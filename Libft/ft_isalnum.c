#include "libft.h"

int	ft_isalnum(int i)
{
	if (!(i >= '0' && i <= '9'))
	{
		if (!(i >= 'a' && i <= 'z'))
		{
			if (!(i >= 'A' && i <= 'Z'))
			{
				return (0);
			}
		}
	}
	return (1);
}
